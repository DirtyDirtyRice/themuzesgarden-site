import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const dateArg = process.argv[2] || new Date().toISOString().slice(0, 10);
const safeDate = dateArg.replace(/[^0-9-]/g, "");
const outDir = path.join(root, "codex-session-notes", safeDate + "-coding-app");

function runGit(args) {
  try {
    return execFileSync("git", args, {
      cwd: root,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "pipe"],
    });
  } catch (error) {
    return error.stdout?.toString() || error.stderr?.toString() || String(error);
  }
}

fs.mkdirSync(outDir, { recursive: true });

const status = runGit(["status", "--short"]);
const diff = runGit(["diff"]);
const stagedDiff = runGit(["diff", "--cached"]);
const changedNames = runGit(["status", "--short"])
  .split(/\r?\n/)
  .map((line) => line.trim())
  .filter(Boolean);

const readme = [
  "# Codex Daily Save",
  "",
  `Date: ${safeDate}`,
  "",
  "## Purpose",
  "",
  "Developer handoff package for today's Codex work.",
  "",
  "## Current Rule",
  "",
  "Finder first. Proof edit second. Real edit third.",
  "",
  "## Files Changed",
  "",
  changedNames.length ? changedNames.map((line) => `- ${line}`).join("\n") : "No uncommitted file changes found.",
  "",
  "## Notes",
  "",
  "- Generated by scripts/codex-daily-save.mjs",
  "- git-diff.patch contains unstaged code changes.",
  "- git-staged-diff.patch contains staged code changes.",
  "- git-status.txt shows current working tree state.",
  "",
].join("\n");

fs.writeFileSync(path.join(outDir, "README.md"), readme);
fs.writeFileSync(path.join(outDir, "changed-files-list.txt"), changedNames.join("\n") + "\n");
fs.writeFileSync(path.join(outDir, "git-status.txt"), status);
fs.writeFileSync(path.join(outDir, "git-diff.patch"), diff);
fs.writeFileSync(path.join(outDir, "git-staged-diff.patch"), stagedDiff);
fs.writeFileSync(
  path.join(outDir, "commands-used.md"),
  [
    "# Commands Used",
    "",
    "Add important PowerShell commands from today here as needed.",
    "",
    "```powershell",
    "node scripts/code-map-snapshot.mjs",
    "node scripts/codex-daily-save.mjs " + safeDate,
    "```",
    "",
  ].join("\n")
);

console.log("Daily save package complete.");
console.log("Folder:");
console.log(outDir);
console.log("Files:");
for (const file of fs.readdirSync(outDir)) {
  console.log("- " + file);
}
